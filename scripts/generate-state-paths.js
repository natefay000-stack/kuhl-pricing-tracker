#!/usr/bin/env node
/**
 * Generate US state SVG paths from US Atlas TopoJSON (10m resolution, Albers USA projection).
 * Output: updated src/lib/us-state-paths.ts
 */

const { feature } = require('topojson-client');
const { geoPath, geoIdentity } = require('d3-geo');
const fs = require('fs');
const path = require('path');

// Load pre-projected Albers USA TopoJSON
const topoPath = path.join(__dirname, '..', 'node_modules', 'us-atlas', 'states-albers-10m.json');
const topo = JSON.parse(fs.readFileSync(topoPath, 'utf8'));

// Convert TopoJSON to GeoJSON features
const states = feature(topo, topo.objects.states);

// FIPS code -> state abbreviation mapping
const FIPS_TO_ABBR = {
  '01': 'AL', '02': 'AK', '04': 'AZ', '05': 'AR', '06': 'CA',
  '08': 'CO', '09': 'CT', '10': 'DE', '11': 'DC', '12': 'FL',
  '13': 'GA', '15': 'HI', '16': 'ID', '17': 'IL', '18': 'IN',
  '19': 'IA', '20': 'KS', '21': 'KY', '22': 'LA', '23': 'ME',
  '24': 'MD', '25': 'MA', '26': 'MI', '27': 'MN', '28': 'MS',
  '29': 'MO', '30': 'MT', '31': 'NE', '32': 'NV', '33': 'NH',
  '34': 'NJ', '35': 'NM', '36': 'NY', '37': 'NC', '38': 'ND',
  '39': 'OH', '40': 'OK', '41': 'OR', '42': 'PA', '44': 'RI',
  '45': 'SC', '46': 'SD', '47': 'TN', '48': 'TX', '49': 'UT',
  '50': 'VT', '51': 'VA', '53': 'WA', '54': 'WV', '55': 'WI',
  '56': 'WY', '72': 'PR'
};

const projection = geoIdentity();
const pathGenerator = geoPath(projection);

const pathEntries = {};
for (const feat of states.features) {
  const fips = feat.id;
  const abbr = FIPS_TO_ABBR[fips];
  if (!abbr) continue;
  const d = pathGenerator(feat);
  if (d) {
    const rounded = d.replace(/-?\d+\.\d+/g, (m) => parseFloat(m).toFixed(1));
    pathEntries[abbr] = rounded;
  }
}

const sorted = Object.keys(pathEntries).sort();

let output = `// US State SVG paths — source: US Census Bureau via us-atlas (10m resolution, Albers USA)\n`;
output += `// Generated by scripts/generate-state-paths.js\n`;
output += `// ViewBox: 0 0 960 600\n`;
output += `export const STATE_PATHS: Record<string, string> = {\n`;
for (const abbr of sorted) {
  output += `  ${abbr}: "${pathEntries[abbr]}",\n`;
}
output += `};\n`;

console.log(`Generated paths for ${sorted.length} states`);
const fileSize = Buffer.byteLength(output, 'utf8');
console.log(`File size: ${(fileSize / 1024).toFixed(1)} KB`);
fs.writeFileSync(path.join(__dirname, '..', 'src', 'lib', 'us-state-paths.ts'), output);
console.log('Wrote src/lib/us-state-paths.ts');
